// Helper pour construire une date ISO de la semaine courante
function rdvDate(offsetDays: number, heure: string): string {
  const today = new Date()
  const jour = today.getDay()
  const diffLundi = jour === 0 ? -6 : 1 - jour
  const lundi = new Date(today)
  lundi.setDate(today.getDate() + diffLundi + offsetDays)
  const dateStr = lundi.toISOString().split('T')[0]
  return dateStr
}

